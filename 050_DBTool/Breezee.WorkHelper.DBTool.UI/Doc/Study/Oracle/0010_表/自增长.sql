/*1、先创建序列：从10000开始，每次增长1。*/
CREATE SEQUENCE SEQ_PK_PS_QJ_SH_BAK MINVALUE 1 MAXVALUE 999999999999999999999999999 START WITH 10000 INCREMENT BY 1 CACHE 20;

/*2、再利用新增记录前的触发器调用序列的下一个值方法赋给主键，即可实现自增长*/
CREATE OR REPLACE TRIGGER TRG_PK_PS_QJ_SH_BAK
 BEFORE INSERT ON PS_QJ_SH_BAK
 FOR EACH ROW
BEGIN
 SELECT SEQ_PK_PS_QJ_SH_BAK.NEXTVAL INTO :NEW.RECORDNO FROM DUAL;
END;

